<div class="name">About</div>

This is a bit more interactive version of the
<a href="https://reversed.top/2016-08-13/big-list-of-vim-like-software/">The big list of Vi[m]-like software</a>
post.

<br/><br/>

Straight list form is not the most convenient one to neither view nor update a
large collection of items.  This is an attempt to organize them in a better
way.

<br/><br/>

The two versions aren't in perfect sync, this version received some minor
additions/corrections during transformation and then some.

